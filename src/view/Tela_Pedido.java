    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        Classe_Pedido pedido = new Classe_Pedido();
        DAO_Pedido dao = new DAO_Pedido();
        DAO_Cliente daoC = new DAO_Cliente();
        DAO_Refeicao daoR = new DAO_Refeicao();
        Classe_Cliente cliente = new Classe_Cliente();
        Classe_Refeicao refeicao = new Classe_Refeicao();
        
        
        cliente = daoC.pesquisar(login);
        pedido.setCliente(cliente);
       // refeicao = daoR.selecionar_refeicao();
        pedido.setQuantidade(Integer.parseInt(C_quantidade.getText()));
        pedido.setObservacao(C_obs.getText());
        pedido.setCliente(cliente);
        pedido.setRefeicao(refeicao);
        
        boolean save = dao.save(pedido);
        if(save){
            JOptionPane.showMessageDialog(null, "Pedido realizado com sucesso!");
            this.dispose();
            new Tela_Usuario(login).setVisible(true);
        }
        else{
            JOptionPane.showMessageDialog(null, "ERRO!");
        }
        
        
    }//GEN-LAST:event_jButton1ActionPerformed

