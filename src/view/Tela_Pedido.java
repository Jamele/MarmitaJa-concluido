    public void showCardapio() {
        DefaultTableModel modelo = (DefaultTableModel) cardapio.getModel();
        DAO_Refeicao refeicao = new DAO_Refeicao();
        
        for (Classe_Refeicao r: refeicao.findAll()){
            modelo.addRow(new Object[]{
                r.getIngrediente1(),
                r.getIngrediente2(),
                r.getIngrediente3(),
                r.getIngrediente4()
            });
        }
    }

